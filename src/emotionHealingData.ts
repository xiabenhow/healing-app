// ===================== TYPE DEFINITIONS =====================

export type EmotionKey =
  | 'anxious' | 'tired' | 'low' | 'calm' | 'warm' | 'energized'
  | 'lonely' | 'confused' | 'wronged' | 'escape' | 'insecure' | 'restart';

export type EmotionLevel = 'L1' | 'L2' | 'L3';

export type SoundscapeKey =
  | 'rain' | 'ocean' | 'forest' | 'fireplace' | 'breathing'
  | 'meditation' | 'piano' | 'insects' | 'stream' | 'ambient'
  | 'birds' | 'thunder_rain';

export type BlendType = 'spray' | 'diffuser';

export interface AromaBlend {
  type: BlendType;
  recipe: string;
  oils: string[];
  note: string; // 芳療師說明
}

export interface BedtimeRitual {
  scent: string;
  scentNote: string;
  usage: string;
  actions: string[];
  mindset: string;
  whispers: string[];
}

export interface Soundscape {
  key: SoundscapeKey;
  label: string;
  reason: string;
  suitableTime: '白天' | '傍晚' | '睡前' | '任何時候';
  layerSuggestion?: string; // 疊加建議
}

export interface EmotionLevelData {
  emotion: EmotionKey;
  emotionLabel: string;
  level: EmotionLevel;
  levelLabel: string;
  levelDescription: string;
  subEmotions: string[];
  blend: AromaBlend;
  usage: string[];
  practicalTips: string[];
  actionGuide: string[];
  mindsetGuide: string[];
  warmMessages: string[];
  nightFeedback: string[];
  bedtimeRitual: BedtimeRitual;
  soundscape: Soundscape;
}

export interface SubEmotionItem {
  key: string;
  label: string;
  emoji: string;
}

export interface MainEmotion {
  key: EmotionKey;
  label: string;
  emoji: string;
  color: string;
  description: string;
  subEmotions: SubEmotionItem[];
  levelDescriptions: {
    L1: string;
    L2: string;
    L3: string;
  };
}

