export function getHealingData(emotion: EmotionKey, level: EmotionLevel): EmotionLevelData | undefined {
  return EMOTION_HEALING_DATA.find(d => d.emotion === emotion && d.level === level);
}

/** 根據情緒 key 取得主情緒資訊 */
export function getMainEmotion(emotion: EmotionKey): MainEmotion | undefined {
  return MAIN_EMOTIONS.find(e => e.key === emotion);
}

/** 從暖心句子中隨機取一句（避免重複感） */
export function getRandomWarmMessage(emotion: EmotionKey, level: EmotionLevel): string {
  const data = getHealingData(emotion, level);
  if (!data) return '你很好，一切都會好的';
  const messages = data.warmMessages;
  return messages[Math.floor(Math.random() * messages.length)];
}

/** 從晚間回饋中隨機取一個問題 */
export function getRandomNightFeedback(emotion: EmotionKey, level: EmotionLevel): string {
  const data = getHealingData(emotion, level);
  if (!data) return '今天還好嗎？';
  const questions = data.nightFeedback;
  return questions[Math.floor(Math.random() * questions.length)];
}

/** 從睡前低語中隨機取一句 */
export function getRandomWhisper(emotion: EmotionKey, level: EmotionLevel): string {
  const data = getHealingData(emotion, level);
  if (!data) return '晚安';
  const whispers = data.bedtimeRitual.whispers;
  return whispers[Math.floor(Math.random() * whispers.length)];
}

